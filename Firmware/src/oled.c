/*** Static Functions ********************************************************/
/// @brief Copies Data from the 8x8 font to the given data buffer pointer
/// @param str, string to append
/// @param ptr, Pointer to the data buffers position to append to
/// @return None
static void font8x8_to_framebuffer(const char *str, uint8_t *ptr)
{
	if(str == NULL || ptr == NULL) return;
	
	while(*str)
	{
		// Find the desired char in the small font LUT (incriment str too)
		const char *lut_ptr = strchr(font8x8_lut, *str++);
		if(lut_ptr == NULL) continue;
		
		// Copy the font data for the given char to the framebuffer
		memcpy(ptr, font8x8_data[lut_ptr - font8x8_lut], sizeof(font8x8_data[0]));
		ptr += sizeof(font8x8_data[0]);
	}
}


/// @brief Copies Data from the 16x16 font to the given data buffer pointer
/// @param str, string to append
/// @param ptr, Pointer to the data buffers position to append to
/// @return None
/// NOTE: This has no overflow protection, because I lost the will to care. 
/// Don't be a dumbshit when using this function :)
static void font16x16_to_framebuffer(const char *str, uint8_t *ptr)
{
	if(str == NULL || ptr == NULL) return;


	#define GLYPH_ROWS (sizeof(font16x16_data[0]) / sizeof(font16x16_data[0][0]))
	while(*str)
	{
		// Find the desired char in the large font LUT (incriment str too)
		const char *lut_ptr = strchr(font16_16_lut, *str++);
		if(lut_ptr == NULL) continue;
		
		// Copy the font data for the given char to the framebuffer
		// Draw data[0] above data[1], etc with data[2] above data[3]
		const uint16_t *glyph = font16x16_data[lut_ptr - font16_16_lut];
		for(uint8_t line = 0; line < GLYPH_ROWS; line++)
		{
			ptr[line]              = (uint8_t)(glyph[line] & 0xFF);
			ptr[line + OLED_WIDTH] = (uint8_t)(glyph[line] >> 8);
		}


		ptr += GLYPH_ROWS;
	}
}


/// @brief Converts a given millivolt/milliamp value to a 2 decimal place
/// formatted string at the given pointer
/// @param val, Milli[x] range value
/// @param str, pointer to the first char of the string to append to
/// @return None
static void append_formatted_2dp_string(const uint16_t val, char *str)
{
	uint16_t whole = val / 1000;          // volts
	uint16_t frac  = (val % 1000) / 10;   // hundredths of a volt

	str[0] = (whole / 10) + '0';
	str[1] = (whole % 10) + '0';
	str[2] = '.';
	str[3] = (frac / 10) + '0';
	str[4] = (frac % 10) + '0';
}


/// @brief Converts a given value to a 3 digit, zero padded string
/// @param val, value to convert (999 max)
/// @param str, pointer to the first char of the string to append to
static void append_padded_3digit_string(const uint16_t val, char *str)
{
	str[0] = '0' + ((val / 100) % 10);
	str[1] = '0' + ((val / 10)  % 10);
	str[2] = '0' +  (val % 10);	
}



/*** Hardware Function Definitions *******************************************/
oled_err_t oled_init(const i2c_device_t *dev)
{
	if(dev == NULL) return OLED_INVALID_INPUT;

	// Set the static i2c device value to the given device
	_oled_dev = dev;

	const uint8_t init_arr[] = {
		0xAE,			// Display OFF
		0xD5, 0xF0,		// Clock
		0xA8, 0x3F,		// Multiplex (64)
		0xD3, 0x00,		// Offset
		0x40,			// Start line
		0x8D, 0x14,		// Charge pump
		0x20, 0x00,		// Horizontal addressing
		0xA1,			// Segment remap
		0xC8,			// COM scan dec
		0xDA, 0x12,		// COM pins
		0x81, 0x7F,		// Contrast
		0xD9, 0x22,		// Pre-charge
		0xDB, 0x40,		// VCOM detect
		0xA4,			// Resume RAM
		0xA6,			// Normal display
		0xAF			// Display ON
	};
	
	// Write the Initialisation Array to the Display
	OLED_WRITE(OLED_REG_COMMAND, init_arr,  sizeof(init_arr));
	
	return OLED_OK;
}


oled_err_t oled_set_cursor(const uint8_t x, const uint8_t y)
{
	if(x >= OLED_WIDTH || y >= OLED_PAGES) 
		return OLED_INVALID_INPUT;

	uint8_t pos_arr[3] = { (0xB0 | y), 
						   (0x00 | (x & 0x0F)), 
						   (0x10 | (x >> 4)) };
	// Set Page Address
	OLED_WRITE(OLED_REG_COMMAND, &pos_arr[0], 1);
	// Set lower column address
	OLED_WRITE(OLED_REG_COMMAND, &pos_arr[1], 1);
	// Set upper column address
	OLED_WRITE(OLED_REG_COMMAND, &pos_arr[2], 1);

	return OLED_OK;
}


oled_err_t oled_update(void)
{
	if(_oled_dev == NULL) return OLED_UNSET_DEVICE;
	
	// Write the OLED Display Data
	for(int page = 0; page < OLED_PAGES; page++)
	{	
		oled_set_cursor(0, page);
		OLED_WRITE(OLED_REG_DISPLAY_START, _oled_buffer[page], OLED_WIDTH);
	}

	return OLED_OK;
}




